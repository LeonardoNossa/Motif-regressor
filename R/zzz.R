#' @title MotifRegressR: Perform motif regression on gene expression conditions
#'
#' @description This R package facilitates motif analysis
#' for gene expression studies. It integrates upstream DNA sequence information,
#' gene expression profiles, and Position Frequency Matrices (PFMs)
#' to identify regulatory motifs influencing gene expression.
#' Users can import PFMs from JASPAR or custom sources,
#' annotate genomic sequences with transcription start sites (TSS),
#' and retrieve metadata of gene expression conditions.
#' The package scores motifs across genes, aggregates the results,
#' and performs regression analysis to determine the most impactful motifs
#' under different experimental conditions. Visualization functions finally
#' help the user in the sense-making step of the analyses.
#' @name MotifRegressR
#' @keywords package
